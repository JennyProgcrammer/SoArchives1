        document.getElementById('seller-terms-btn').addEventListener('click', function() {
            document.getElementById('seller-terms').style.display = 'block';
            document.getElementById('buyer-terms').style.display = 'none';
            document.getElementById('privacy-policy').style.display = 'none';
        });

        document.getElementById('buyer-terms-btn').addEventListener('click', function() {
            document.getElementById('seller-terms').style.display = 'none';
            document.getElementById('buyer-terms').style.display = 'block';
            document.getElementById('privacy-policy').style.display = 'none';
        });

        document.getElementById('privacy-policy-btn').addEventListener('click', function() {
            document.getElementById('seller-terms').style.display = 'none';
            document.getElementById('buyer-terms').style.display = 'none';
            document.getElementById('privacy-policy').style.display = 'block';
        });

        document.addEventListener('DOMContentLoaded', function() {
            const buttons = document.querySelectorAll('.list-group-item');

            buttons.forEach(button => {
                button.addEventListener('click', function() {
                    // Remove 'active' class from all buttons
                    buttons.forEach(btn => btn.classList.remove('active'));
                    // Add 'active' class to the clicked button
                    this.classList.add('active');

                    // Optionally, display corresponding content
                    showSection(this.id);
                });
            });

            function showSection(id) {
                // Hide all sections
                const sections = document.querySelectorAll('.content-section');
                sections.forEach(section => section.style.display = 'none');

                // Show the selected section
                if (id === 'seller-terms-btn') {
                    document.getElementById('seller-terms').style.display = 'block';
                } else if (id === 'buyer-terms-btn') {
                    document.getElementById('buyer-terms').style.display = 'block';
                } else if (id === 'privacy-policy-btn') {
                    document.getElementById('privacy-policy').style.display = 'block';
                }
            }

            // Initially show the first section
            showSection('seller-terms-btn');
        });
